#include <iostream> //Pre-processor directive to include the iostream file for inputs and outputs
using namespace std; //Declares the use of standard namespace

int main() { //Main function
    double numberOne = 50; //Declaring the first variable storing the first number
    double numberTwo = 7; // Declaring the second variable storing the second number
    cout << numberOne / numberTwo << endl; //Dividing the first by the second number and outputting the result

    return 0;// The return or exit statement
}