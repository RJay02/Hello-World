#include <iostream> //Pre-processor directive to include the iostream file for inputs and outputs
using namespace std; //Declares the use of standard namespace

int main() { //The main function 
    int var1 = 8; //Declaring the first integer variable with a value of 8
    int var2 = 10; //Declaring the second integer variable with a value of 10
    int var3 = var1 + var2; //Third integer variable adding the previous two variables together
    cout << var3 << endl; //Outputing the sum to the console
    return 0; //Return statement

}